    private void placeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_placeActionPerformed
        String odrID = inputOdrID.getText();
        String cusID = inputCusID.getText();
        String size = inputSize.getText();
        int qty = Integer.parseInt(inputQTY.getText());
        double amount = Double.parseDouble(PrintAmount.getText());

        CustomerList.add(new CustomerDetails(odrID, cusID, amount, qty, size, status[0]));

        JOptionPane.showMessageDialog(this, "Order Placed");
        order++;
        String Sodr = String.format("%05d", order);
        odr = "ODR#" + Sodr;
        inputOdrID.setText(odr);
        inputCusID.setText("");
        inputSize.setText("");
        inputQTY.setText("");
        PrintAmount.setText("");

        inputCusID.requestFocus();
    }//GEN-LAST:event_placeActionPerformed

