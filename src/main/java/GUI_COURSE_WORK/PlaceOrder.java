    private void inputCusIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputCusIDKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER || evt.getKeyCode() == KeyEvent.VK_TAB) {
            if (inputCusID.getText().charAt(0) != '0' || inputCusID.getText().length() != 10) {
                JOptionPane.showMessageDialog(this, "Invalid Customer ID");
            } else {
                inputSize.requestFocus();
            }
        }
    }//GEN-LAST:event_inputCusIDKeyPressed

    private void inputSizeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputSizeKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER || evt.getKeyCode() == KeyEvent.VK_TAB) {
            String insertSize = inputSize.getText().toLowerCase();
            if (insertSize.equals("xs") || insertSize.equals("s") || insertSize.equals("m") || insertSize.equals("l") || insertSize.equals("xl") || insertSize.equals("xxl")) {
                inputQTY.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Size");
            }
        }
    }//GEN-LAST:event_inputSizeKeyPressed

    private void inputQTYKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputQTYKeyReleased
        if (inputQTY.getText().matches("\\d+")) {
            int qty = Integer.parseInt(inputQTY.getText());

            double amount = 0;
            switch (inputSize.getText().toLowerCase()) {
                case "xs":
                    amount = (600 * qty);
                    break;
                case "s":
                    amount = (800 * qty);
                    break;
                case "m":
                    amount = (900 * qty);
                    break;
                case "l":
                    amount = (1000 * qty);
                    break;
                case "xl":
                    amount = (1100 * qty);
                    break;
                case "xxl":
                    amount = (1200 * qty);
                    break;
                default:
                    break;
            }

            PrintAmount.setText(amount + "");

        } else {

        }


    }//GEN-LAST:event_inputQTYKeyReleased

